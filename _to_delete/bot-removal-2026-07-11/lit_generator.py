def generate_character_for_lit(genre: dict) -> dict:
    """根據情感文學類別生成主角。"""
    protagonist_type = genre.get("protagonist", "普通好人")
    gender = random.choice(["男", "女"])
    occupation = random.choice(_OCCUPATION_POOLS.get(protagonist_type, ["普通人"]))
    surname = random.choice(_COMPOUND_SURNAMES) if random.random() < 0.15 else random.choice(_SURNAMES)
    firstname = random.choice(_MALE_NAMES if gender == "男" else _FEMALE_NAMES)
    return {
        "name": surname + firstname,
        "gender": gender,
        "occupation": occupation,
        "protagonist_type": protagonist_type,
    }


def generate_lit_story(genre: dict, character: dict, max_retries: int = 3):
    """生成情感文學故事，返回 (content, style_name, mood)。"""
    client = OpenAI(api_key=DEEPSEEK_API_KEY, base_url="https://api.deepseek.com")

    style = random.choice(AUTHOR_STYLES)
    mood = genre.get("mood", "溫暖")
    mood_guide = MOOD_GUIDES.get(mood, "")
    protagonist_guide = PROTAGONIST_TEMPLATES.get(genre.get("protagonist", "普通好人"), "")

    # 抓實時 trending 素材（失敗唔影響主流程）
    trending_hint = ""
    try:
        trending_hint = fetch_trending_topics("L")
    except Exception as e:
        print(f"[lit_generator] trending 抓取失敗（非致命）：{e}")

    _trend = (
        f"\n【今日社會情緒參考（可自然融入 1 個最合適的）】\n{trending_hint}\n"
        if trending_hint else ""
    )

    prompt = f"""你是頂尖中文網文作家，此刻要寫一篇讓讀者放下手機後還在回想的故事。

【故事設定】
類別：{genre.get('name', '')}
主題：{genre.get('theme', '')}
情緒基調：{mood}
主角類型：{genre.get('protagonist', '')}
故事梗概：{genre.get('desc', '')}

【主角】
姓名：{character['name']}（{character['gender']}）
職業：{character['occupation']}
氣質：{protagonist_guide}

【開場第一句（必須使用，從此展開故事）】
{genre.get('hook', '')}

【全篇靈魂場景（所有情節都在為這一刻鋪墊）】
{genre.get('key_scene', '')}
→ 這個場景必須用「慢鏡頭」寫：拉長時間、具體感官細節（聲音、觸感、光線、氣味）——讀者要在這裡停留，感受到重量

【情緒基調指南】
{mood_guide}

【寫作風格（貫穿全文）】
參考作家：{style['ref']}
{style['guide']}
{_trend}
【寫作要求】
1. 開場第一句已給定，請從這一句展開，不要修改
2. 禁止：打臉亮身份、流水線逆轉、主角當眾贏得掌聲
3. 情感要落在細節裡，不靠旁白告訴讀者「主角很傷心」
4. 結局必須呼應開場的那一句或那個場景（首尾成環）
5. 字數：3000-4500 字
6. 繁體中文，直接從標題開始寫

開始："""

    last_err = None
    for attempt in range(1, max_retries + 1):
        try:
            response = client.chat.completions.create(
                model="deepseek-chat",
                messages=[{"role": "user", "content": prompt}],
                temperature=1.1,
                max_tokens=7000,
            )
            return response.choices[0].message.content, style["name"], mood
        except Exception as e:
            last_err = e
            print(f"[generate_lit_story] 第 {attempt} 次失敗：{e}")
    raise RuntimeError(f"生成失敗（已重試 {max_retries} 次）：{last_err}")


def generate_and_send_lit(genre_name: str | None = None):
    """按需生成並發送情感文學故事。供 bot_listener.py 調用。"""
    if genre_name:
        genre = next((g for g in LIT_GENRES if g["name"] == genre_name), None) or random.choice(LIT_GENRES)
    else:
        data = load_genre_data()
        recent = set(data.get("recent_genres", []))
        unseen = [g for g in LIT_GENRES if g["name"] not in recent]
        genre = random.choice(unseen) if unseen else random.choice(LIT_GENRES)

    character = generate_character_for_lit(genre)
    send_telegram(f"📚 [情感文學] 生成中：{genre['name']} · {character['name']}，請稍候...")

    content, style_name, mood = generate_lit_story(genre, character)
    print(f"[generate_and_send_lit] 完成，字數={len(content)}")

    # 更新 recent_genres 記錄（與爽文共用池，避免兩邊都連續出同一個名）
    try:
        data = load_genre_data()
        recent = data.get("recent_genres", [])
        if genre["name"] in recent:
            recent.remove(genre["name"])
        recent.append(genre["name"])
        data["recent_genres"] = recent[-30:]
        save_genre_data(data)
    except Exception as e:
        print(f"[generate_and_send_lit] 更新記錄失敗：{e}")

    header = (
        f"📚 [情感文學]  {genre['name']}\n"
        f"👤 {character['name']} · {character['gender']} · {character['occupation']}\n"
        f"🎭 {mood}  ·  {style_name}\n"
        f"━━━━━━━━━━━━━━━━━━━━\n\n"
    )

    rating_keyboard = {
        "inline_keyboard": [[
            {"text": "😞 差",   "callback_data": f"ratex_1_{genre['name']}"},
            {"text": "😐 一般", "callback_data": f"ratex_2_{genre['name']}"},
            {"text": "😊 好",   "callback_data": f"ratex_3_{genre['name']}"},
            {"text": "🤩 超好", "callback_data": f"ratex_4_{genre['name']}"},
        ]]
    }

    # 存盤到今日故事檔案，供 /menu 重讀
    try:
        from utils import save_story_to_disk
        save_story_to_disk({
            "type": "lit",
            "genre": genre["name"],
            "mood": mood,
            "style": style_name,
            "character": character,
            "content": content,
        })
    except Exception as e:
        print(f"[generate_and_send_lit] save_story_to_disk 失敗（非致命）：{e}")

    send_telegram(header + content, reply_markup=rating_keyboard)
    print("[generate_and_send_lit] send_telegram 完成")


